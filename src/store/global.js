// ==|== Imports ===================================================================================
import { defineStore } from 'pinia';

// ==|== Store =====================================================================================
export const useGlobalStore = defineStore('global', {
  // ==|== State ===================================================================================
  state: () => {
    return {
      count: 0
    };
  },

  // ==|== Actions =================================================================================
  actions: {
    increment() {
      this.count++;
    }
  },

  // ==|== Getters =================================================================================
  getters: {
    doubleCount: (state) => state.count * 2
  }
});
